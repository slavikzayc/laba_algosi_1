"""
Radix sort (поразрядная сортировка) — идея:

1) Любое число можно представить как набор "цифр" в некотором основании `base`.
2) Делаем несколько проходов по разрядам (обычно с младшего — LSD): сортируем по ОДНОЙ цифре за раз.
3) Очень важно: сортировка на каждом проходе должна быть СТАБИЛЬНОЙ, чтобы не ломать порядок,
   полученный на предыдущих (младших) разрядах.

В этой задаче:
- `n = len(arr)` и это же основание системы счисления (`base = n`).
- Числа предполагаются в диапазоне `[0, n^3 - 1]`, то есть у каждого числа ровно 3 "цифры" в базе `n`:
  разряды `n^0`, `n^1`, `n^2`.
- Поэтому делаем 3 прохода `k = 0, 1, 2`.
- Для простоты используем "корзины" (buckets): это проще понять, чем counting sort с префиксными суммами.
"""


def radix_sort_n3(arr):  # функция сортировки массива arr
    n = len(arr)  # n = количество элементов; и одновременно основание системы счисления (base = n)

    if n <= 1:  # если массив длины 0 или 1 — он уже отсортирован
        return arr[:]  # arr[:] — "срез всего списка": создаёт НОВЫЙ список (копию) с теми же элементами

    output = arr[:]  # рабочая копия массива, чтобы не менять исходный arr

    for k in range(3):  # номер разряда в базе n: 0 -> n^0 (младший), 1 -> n^1, 2 -> n^2 (старший)
        buckets = [[] for _ in range(n)]  # n корзин под цифры 0..n-1 (buckets[d] — элементы с digit=d)

        base_pow = n ** k  # заранее считаем n^k (вес текущего разряда)

        for num in output:  # идём слева направо: это помогает сохранить стабильность внутри каждой корзины
            digit = (num // base_pow) % n  # берём k-ю "цифру" числа num в системе с основанием n
            buckets[digit].append(num)  # кладём num в соответствующую корзину; append сохраняет порядок (стабильно)

        output = [num for bucket in buckets for num in bucket]  # склеиваем корзины 0..n-1 обратно в один список

    return output  # после 3 стабильных проходов по разрядам получаем полностью отсортированный массив


